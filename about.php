తెలుగువారి జానపద కళారూపాలు, వాటి సంబంధిత వీడియాలు, ఫోటోలు, పుస్తకాలు, అంతర్జాల వనరులు  అందరికీ అందుబాటులోకి తీసుకురావటమే ఈ "తానా తందానా"  వెబ్ అప్లికేషన్ ప్రధాన ఉద్దేశము.
<br><br>
ఆసియా పసిఫిక్ ఫౌండేషన్ వారు, మిన్నసోటా తెలుగు సంఘం వారి సౌజన్యముతో,   "తానా తందానా: తెలుగువారి జానపద కళల పరిచయము" అనే ఈ-పుస్తకము, వివిధ దశల్లో తీసుకురావాలనే లక్ష్యము, ప్రణాళికలు కూడా ఉన్నాయి.
<br><br>
ఈ వెబ్ సైట్లో లేని తెలుగు వారి జానపద కళలు మీకేమైనా తెలిస్తే మాకు తెలియచేయండి.    కొత్త వీడియాలు, ఫోటోలు, అంతర్జాల వనరులు,  ఏదైనా అదనపు సమాచారం మీ దగ్గర ఉంటే, మాకు తెలియచేయండి.
